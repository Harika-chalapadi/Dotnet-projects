using Microsoft.EntityFrameworkCore;
using HarikaWebApplication2.Models;

namespace HarikaWebApplication2
{
    class Program
    {
        static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.
            builder.Services.AddControllersWithViews();

            var ConnectionStr = builder.Configuration.GetConnectionString("ConnectionStr");
            builder.Services.AddDbContext<AppDbContext>(options => options.UseSqlServer(ConnectionStr));

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (!app.Environment.IsDevelopment())
            {
                app.UseExceptionHandler("/Home/Error");
            }
            app.UseStaticFiles();

            app.UseRouting();

            app.UseAuthorization();

            app.MapControllerRoute(
                name: "default",
                pattern: "{controller=Products}/{action=Index}/{id?}");

            app.Run();
        }
    }


}
